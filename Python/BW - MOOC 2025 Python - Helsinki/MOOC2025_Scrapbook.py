# WIP || Type in a number. Print out factorial. (i.. 5*4*3*2*1)

number = int(input("Please type in a number: "))

while number > 0:
    i = 0
    while i > number: 
        yVal = {number * i}
        
    print(f"The factorial of the number {number} is {yVal}")
    break